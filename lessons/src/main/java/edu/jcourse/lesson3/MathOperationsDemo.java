package edu.jcourse.lesson3;

public class MathOperationsDemo {

    public static void main(String[] args) {
        int i = 10;
        System.out.println("i1: " + ++i);
        System.out.println("i2: " + i++);
        System.out.println(i);

        i++;
        i = i + 1;

        i--;
        System.out.println(i);
    }
}
